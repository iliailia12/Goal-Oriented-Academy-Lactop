# 1)მოიყვანეთ აბსოლიტურად ყველა მათემატიკურ  ოპერატორზე სამ სამი მაგალითი ძველებზეც და ახლებზეც





# 1) + ოპერატორი (ჯამი)
# აღწერა: ორი ან მეტი რიცხვის გაერთიანება, ანუ ჯამის გამოთვლა
print(5 + 3)     
print(10 + 20)    
print(7 + 2 + 1)  

# 2) - ოპერატორი (გამოკლება)
# აღწერა: ერთი რიცხვისგან მეორის გამოკლება
print(10 - 4)     
print(20 - 5)     
print(7 - 3 - 2) 

# 3) * ოპერატორი (გამრავლება)
# აღწერა: ორი ან მეტი რიცხვის ნამრავლის გამოთვლა
print(5 * 3)      
print(2 * 4)      
print(3 * 2 * 2) 

# 4) / ოპერატორი (გაყოფა)
# აღწერა: ერთი რიცხვის გაყოფა მეორეზე, პასუხი ყოველთვის float
print(10 / 2)    
print(9 / 3)     
print(7 / 2)   

# 5) // ოპერატორი (ბილოკური გაყოფა)
# აღწერა: გაყოფის შედეგის მთელი ნაწილი
print(10 // 3)   
print(20 // 6)    
print(7 // 2)     

# 6) % ოპერატორი (ნაშთი)
# აღწერა: გაყოფის ნაშთი
print(10 % 3)    
print(20 % 6)    
print(7 % 2)  

# 7) ** ოპერატორი (გამავლობა/სტეპენზე აყვანა)
# აღწერა: ერთი რიცხვის აწევა მეორე რიცხვის ხარისხზე
print(2 ** 3)   
print(5 ** 2)    
print(3 ** 4)   

# დამატებითი ოპერატორები (ზოგი რიცხვითი ახალებია Python-ში)

# 8) += ოპერატორი (ჯამის მატება)
x = 5
x += 3          
print(x)      

# 9) -= ოპერატორი (გამოკლების მატება)
y = 10
y -= 4           
print(y)      

# 10) *= ოპერატორი (გამრავლების მატება)
z = 2
z *= 5         
print(z)     

# 11) /= ოპერატორი (გაყოფის მატება)
a = 20
a /= 4        
print(a)     

# 12) //= ოპერატორი (ბილოკური გაყოფის მატება)
b = 15
b //= 4          
print(b)     

# 13) %= ოპერატორი (ნაშთის მატება)
c = 17
c %= 5         
print(c)    

# 14) **= ოპერატორი (სტეპენის მატება)
d = 3
d **= 3          
print(d)        
