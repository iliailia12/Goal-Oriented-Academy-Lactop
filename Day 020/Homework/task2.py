# დღეს ვისწავლეთ შემდეგი მათემატიკური ოპერაციები:

# 1) + ოპერატორი (ჯამი)
#    აღწერა: ორი ან მეტი რიცხვის გაერთიანება, ანუ ჯამის გამოთვლა.
#    მაგალითი: 5 + 3 = 8

# 2) - ოპერატორი (გამოკლება)
#    აღწერა: ერთი რიცხვისგან მეორის გამოკლება.
#    მაგალითი: 10 - 4 = 6

# 3) * ოპერატორი (გამრავლება)
#    აღწერა: ორი ან მეტი რიცხვის ნამრავლის გამოთვლა.
#    მაგალითი: 3 * 4 = 12

# 4) / ოპერატორი (გაყოფა)
#    აღწერა: ერთი რიცხვის გაყოფა მეორეზე; პასუხი ყოველთვის იქნება float ტიპის.
#    მაგალითი: 10 / 2 = 5.0

# 5) // ოპერატორი (ბილოკური გაყოფა)
#    აღწერა: გაყოფის შედეგად მიღებული **თვითმომართული ნაწილი**.
#    მაგალითი: 7 // 2 = 3

# 6) % ოპერატორი (ნაშთი)
#    აღწერა: გაყოფის შედეგად მიღებული ნაშთი.
#    მაგალითი: 7 % 2 = 1

# 7) ** ოპერატორი (გამავლობა / სტეპენზე აყვანა)
#    აღწერა: ერთი რიცხვის აწევა მეორე რიცხვის ხარისხზე.
#    მაგალითი: 2 ** 3 = 8
