# 1) კომენტარის სახით ჩამოწერეთ  რომელი ახალი მათემატიკური ოპერატორები ვისწავლეთ,თითოეულ მათგანს მიუწერეთ  რომელი რას აკეთებს და თითოზე მოიყვანე სამ-სამი მაგალითი



# 1) + ოპერატორი (ჯამი)
#    აღწერა: ორი ან მეტი რიცხვის გაერთიანება, ანუ ჯამის გამოთვლა
#    მაგალითები:
#    5 + 3 = 8
#    10 + 20 = 30
#    7 + 2 + 1 = 10

# 2) - ოპერატორი (გამოკლება)
#    აღწერა: ერთი რიცხვისგან მეორის გამოკლება
#    მაგალითები:
#    10 - 4 = 6
#    20 - 5 = 15
#    7 - 3 - 2 = 2

# 3) * ოპერატორი (გამრავლება)
#    აღწერა: ორი ან მეტი რიცხვის ნამრავლის გამოთვლა
#    მაგალითები:
#    5 * 3 = 15
#    2 * 4 = 8
#    3 * 2 * 2 = 12

# 4) / ოპერატორი (გამოყოფა)
#    აღწერა: ერთი რიცხვის გაყოფა მეორეზე; პასუხი ყოველთვის იქნება ნაშთის გარეშე (float)
#    მაგალითები:
#    10 / 2 = 5.0
#    9 / 3 = 3.0
#    7 / 2 = 3.5

# 5) // ოპერატორი (ბილოკური გაყოფა)
#    აღწერა: გაყოფის შედეგის მთელი ნაწილი
#    მაგალითები:
#    10 // 3 = 3
#    20 // 6 = 3
#    7 // 2 = 3

# 6) % ოპერატორი (ნაშთი)
#    აღწერა: ერთი რიცხვის გაყოფის ნაშთი მეორეზე
#    მაგალითები:
#    10 % 3 = 1
#    20 % 6 = 2
#    7 % 2 = 1

# 7) ** ოპერატორი (გამავლობა/სტეპენზე აყვანა)
#    აღწერა: ერთი რიცხვის აწევა მეორე რიცხვის ხარისხზე
#    მაგალითები:
#    2 ** 3 = 8
#    5 ** 2 = 25
#    3 ** 4 = 81
