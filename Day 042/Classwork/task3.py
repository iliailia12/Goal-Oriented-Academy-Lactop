# 3) შექმენით ფუნქცია, რომელიც მომხმარებლისგან მიიღებს 5 რიცხვს და დაბეჭდავს მათ საშუალო არითმეტიკულს.
# რიცხვების შემოტანისთვის გამოიყენეთ for ციკლი და input ფუნქცია.




def average_of_5():
    total = 0  

    for i in range(5):
        number = float(input(f"შეიყვანეთ {i+1}-ე რიცხვი: "))
        total += number   

    average = total / 5

    print(f"ჩამოტანილი რიცხვების საშუალო არითმეტიკული არის: {average}")

average_of_5()
