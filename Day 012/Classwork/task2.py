# 2) დაწერეთ მაგალითები შედარების ოპერატორებზე.




# > (მეტობა)
print(5 > 3)    
print(1 > 10)    

# < (ნაკლებობა)
print(2 < 8)    
print(9 < 4)     

# >= (მეტი ან ტოლი)
print(7 >= 7)   
print(3 >= 10)   

# <= (ნაკლები ან ტოლი)
print(4 <= 6)   
print(8 <= 2)    

# == (ტოლობა)
print(5 == 5)   
print(5 == 3)    

# != (არათოლობა)
print(9 != 4)   
print(6 != 6)    
