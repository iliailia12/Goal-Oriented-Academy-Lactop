# 1) დაწერეთ 2-2 მაგალითი შედარების ოპერატორებზე(>, <, <=, >=, ==, !=)




# >  (მეტობა)
print(5 > 3)  
print(10 > 20) 

# <  (ნაკლებობა)
print(2 < 8)   
print(15 < 7)   

# <=  (ნაკლები ან ტოლი)
print(4 <= 4)  
print(6 <= 2)   

# >=  (მეტი ან ტოლი)
print(9 >= 3) 
print(5 >= 10)  

# ==  (ტოლობა)
print(7 == 7)  
print(5 == 8)  

# !=  (არათოლობა)
print(9 != 4)   
print(10 != 10) 
