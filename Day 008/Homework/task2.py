# 3) მომხმარებელს შემოატანინეთ 5 რიცხვი და გაიგეთ მათი საშუალო არითმეტიკული



num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))
num3 = float(input("შეიყვანეთ მესამე რიცხვი: "))
num4 = float(input("შეიყვანეთ მეოთხე რიცხვი: "))
num5 = float(input("შეიყვანეთ მეხუთე რიცხვი: "))

average = (num1 + num2 + num3 + num4 + num5) / 5

print("ამ რიცხვების საშუალო არითმეტიკული არის:", average)
