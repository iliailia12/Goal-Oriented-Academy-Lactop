# 3) მომხმარებელს შემოატანინეთ სახელი input-ის მეშვეობით, თქვენი დაბალებაა შეამოწმოთ არის თუ არა მომხმარიბლის სახელი თქვენს მიერ წინასწარ გამზადებულ სახელების სიაში (სია უნდა იყოს 3 ელემენტიანი ანუ 3 სახელი)

name_list = ["ნიკა", "მარიამ", "გიორგი"]

