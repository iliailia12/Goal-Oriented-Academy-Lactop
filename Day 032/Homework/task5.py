# 10) შექმენი ცარიელი სია. while ციკლის გამოყენებით მომხმარებელს შემოატანინე რიცხვი და სიაში დაამატე იქამდე, სანამ ის ნულს არ შემოიტანს.
# როგორც კი მომხმარებელი შემოიტანს ნულს while ციკლი უნდა გაითიშოს და დაიბეჭდოს მიღებული სია(თვითონ ნული არ უნდა ჩაემატოს)



numbers = []

while True:
    num = int(input("შეიყვანეთ რიცხვი (0 შესვლისას გაჩერება): "))
    
    if num == 0:
        break  
    
    numbers.append(num)  

print(numbers)
