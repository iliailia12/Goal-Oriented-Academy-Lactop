// 5) შექმენით var ცვლადი საწყისი მნიშვნელობით 5. გამოიყენეთ ++ ოპერატორი ორჯერ და საბოლოო შედეგი გამოიტანეთ კონსოლში.

var number = 5;
number++;
number++
console.log(number) //გამოიტანს 7

