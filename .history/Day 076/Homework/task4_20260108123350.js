console.log(number++ + ++number) //..აქ წესედ 8 უნდა გამოიტანოს შუაში პლიუსისი გამო 
console.log(number++ + ++number --) //..აქ წესედ 8 უნდა გამოიტანოს შუაში პლიუსისი გამო 