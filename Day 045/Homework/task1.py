# 1) შექმენი ფუნქცია, რომელიც ითვლის 1-დან 100-მდე რიცხვებიდან იმ რიცხვების რაოდენობას, რომლებიც იყოფა 3-ზე ან 5-ზე, მაგრამ არა ორივეზე ერთად.





# ფუნქციის შექმნა
def count_divisible_by_3_or_5():
    """
    ეს ფუნქცია ითვლის რიცხვებს 1-დან 100-მდე,
    რომლებიც იყოფა 3-ზე ან 5-ზე, მაგრამ არა ორივეზე ერთად.
    """
    count = 0
    for number in range(1, 101):
        if (number % 3 == 0 or number % 5 == 0) and not (number % 3 == 0 and number % 5 == 0):
            count += 1
    return count

# ფუნქციის გამოძახება და შედეგის დაბეჭდვა
result = count_divisible_by_3_or_5()
print(result)  # გამოვა შესაბამისი რაოდენობა
