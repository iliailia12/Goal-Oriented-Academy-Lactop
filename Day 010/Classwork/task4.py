# 4) მოიძიეთ ინფორმაცია if/elif/else-ებზე და შეეცადეთ გააკეთოთ რამოდენიმე მაგალითი.

# if – ხდება, როცა პირობა მართალია
# elif – ხდება, როცა პირველი პირობა მცდარია და ეს მეორე პირობა მართალია
# else – ხდება, როცა არც ერთი პირობა მართალია

age = int(input("შეიყვანეთ თქვენი ასაკი: "))

if age < 18:
    print("თქვენ მცირეწლოვანი ხართ.")
elif age <= 60:
    print("თქვენ ზრდასრულ ხართ.")
else:
    print("თქვენ ხართ მოხუცი ადამიანი.")
